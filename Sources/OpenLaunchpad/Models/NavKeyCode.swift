import Foundation

// MARK: - Navigation Key Codes

/// macOS key codes used for keyboard navigation and actions within OpenLaunchpad.
enum NavKeyCode: UInt16 {
    case leftArrow = 123
    case rightArrow = 124
    case downArrow = 125
    case upArrow = 126
    case `return` = 36
    case pageUp = 116
    case pageDown = 121
    case escape = 53
}
